<h3>Trust Funds</h3>

<p>The Trust funds is generated from contributions made by the founder members.</p>

<p>&nbsp;</p>

<h3>Funds Increases</h3>

<p>&nbsp;</p>

<ul>
	<li>The Principal fund is invested in fixed deposit and half of the interest earned form it is deposited back in the principal amount.</li>
	<li>The Trust can accept contributions made by instutions/organizations without any conditions attached</li>
</ul>

<p>&nbsp;</p>

<h3>Contributing Funds</h3>

<p>&nbsp;</p>

<p>Only half of the interest earned will be contributed as a token to the individuals/institutions/organizations.</p>
