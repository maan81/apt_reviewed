<h3>Our brief introduction</h3>

<p>Inspired by the love for education of our respected parents Mr. Amrit Man Shrestha and Mrs. Prithvi Kumari Shrestha, we have established a non-profit social organization named \Amrit-Prithvi Trust\&#39; with a view to contribute to the nation and people in different fields like education, health, arts, culture, social work and religion.</p>

<p>&nbsp;</p>

<p>This Trust was registered with the District Administrative Office, Kathmandu, Nepal on 3rd July, 1997 (19th Bhadra, 2054 B.S.) under the institute of Organization Registration Act 2034.</p>

